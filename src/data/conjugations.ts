import { verbs } from './verbs';

function buildConjugations(): Record<string, Record<string, string[]>> {
  const result: Record<string, Record<string, string[]>> = {};

  for (const verb of verbs) {
    if (irregulars[verb.id]) {
      result[verb.id] = irregulars[verb.id];
    } else {
      result[verb.id] = conjugateRegular(verb.id, verb.group, verb.auxiliary);
    }
  }

  return result;
}

